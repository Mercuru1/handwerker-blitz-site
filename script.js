function applyTranslations(lang) {
  const dict = i18n[lang];
  document.documentElement.lang = lang === 'de' ? 'de' : lang === 'ru' ? 'ru' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(node => {
    const key = node.dataset.i18n;
    if (dict[key]) node.textContent = dict[key];
  });

  els.langButtons.forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle('active', active);
    btn.setAttribute('aria-pressed', String(active));
  });

  document.title = {
    de: 'Handwerker & Blitz | Handwerker-Service in Krefeld, Viersen & Umgebung',
    ru: 'Handwerker & Blitz | Услуги мастера в Крефельде, Фирзене и рядом',
    en: 'Handwerker & Blitz | Handyman service in Krefeld, Viersen and nearby'
  }[lang];
}

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem('hb-lang', lang);
  applyTranslations(lang);
  renderPortfolio();
}

function setTheme(theme) {
  state.theme = theme;
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem('hb-theme', theme);
}

function toggleTheme() {
  setTheme(state.theme === 'dark' ? 'light' : 'dark');
}

function createPortfolioCard(item, index) {
  const article = document.createElement('article');
  article.className = 'card portfolio-card';
  article.tabIndex = 0;
  article.setAttribute('role', 'button');
  article.setAttribute('aria-label', `${item.title}, ${item.city}`);

  const isSingle = Boolean(item.singleImage);
  const thumbsMarkup = isSingle
    ? `
      <div class="thumbs single">
        <figure><img src="${item.singleImage}" alt="${i18n[state.lang].galleryLabel}: ${item.title}" loading="lazy" decoding="async"></figure>
      </div>
    `
    : `
      <div class="thumbs compare">
        <figure><img src="${item.beforeImage}" alt="${i18n[state.lang].beforeLabel}: ${item.title}" loading="lazy" decoding="async"></figure>
        <figure><img src="${item.afterImage}" alt="${i18n[state.lang].afterLabel}: ${item.title}" loading="lazy" decoding="async"></figure>
      </div>
    `;

  article.innerHTML = `${thumbsMarkup}
    <div class="portfolio-body">
      <div class="portfolio-meta"><strong>${item.category}</strong><span>${item.city}</span></div>
      <h3>${item.title}</h3>
      <p>${item.description[state.lang]}</p>
    </div>
  `;
  article.addEventListener('click', () => openModal(item));
  article.addEventListener('keydown', e => {
    if (e.key === 'Enter' || e.key === ' ') {
      e.preventDefault();
      openModal(item);
    }
  });
  return article;
}

function renderPortfolio() {
  if (!els.portfolioGrid) return;
  els.portfolioGrid.innerHTML = '';
  const visible = state.filter === 'Alle'
    ? state.portfolio
    : state.portfolio.filter(item => item.category === state.filter);

  if (!visible.length) {
    const empty = document.createElement('div');
    empty.className = 'portfolio-empty';
    empty.textContent = i18n[state.lang].portfolioEmpty;
    els.portfolioGrid.appendChild(empty);
    return;
  }

  visible.forEach((item, index) => {
    els.portfolioGrid.appendChild(createPortfolioCard(item, index));
  });
}

function openModal(item) {
  els.modalTitle.textContent = item.title;
  els.modalCategory.textContent = item.category;
  els.modalMeta.textContent = item.city;
  els.modalDescription.textContent = item.description[state.lang];

  const figures = els.modal.querySelectorAll('.modal-images figure');
  const captions = els.modal.querySelectorAll('.modal-images figcaption');

  if (item.singleImage) {
    els.modalBefore.src = item.singleImage;
    els.modalBefore.alt = `${i18n[state.lang].galleryLabel}: ${item.title}`;
    els.modalAfter.removeAttribute('src');
    els.modalAfter.alt = '';
    if (captions[0]) captions[0].textContent = i18n[state.lang].detailLabel;
    if (figures[0]) figures[0].classList.add('single-span');
    if (figures[1]) figures[1].hidden = true;
  } else {
    els.modalBefore.src = item.beforeImage;
    els.modalAfter.src = item.afterImage;
    els.modalBefore.alt = `${i18n[state.lang].modalAltBefore}: ${item.title}`;
    els.modalAfter.alt = `${i18n[state.lang].modalAltAfter}: ${item.title}`;
    if (captions[0]) captions[0].textContent = i18n[state.lang].beforeLabel;
    if (captions[1]) captions[1].textContent = i18n[state.lang].afterLabel;
    if (figures[0]) figures[0].classList.remove('single-span');
    if (figures[1]) figures[1].hidden = false;
  }

  els.modal.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
  els.modalClose.focus();
}

function closeModal() {
  els.modal.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}

async function loadPortfolio() {
  try {
    const response = await fetch('portfolio.json');
    state.portfolio = await response.json();
  } catch (error) {
    console.error('Could not load portfolio.json', error);
    state.portfolio = [];
  }
  renderPortfolio();
}

function initFilters() {
  els.filterButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      state.filter = btn.dataset.filter;
      els.filterButtons.forEach(button => button.classList.toggle('active', button === btn));
      renderPortfolio();
    });
  });
}

function initReveal() {
  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
    document.querySelectorAll('.reveal').forEach(el => el.classList.add('visible'));
    return;
  }

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

function initNav() {
  els.navToggle?.addEventListener('click', () => {
    const expanded = els.navToggle.getAttribute('aria-expanded') === 'true';
    els.navToggle.setAttribute('aria-expanded', String(!expanded));
    els.siteNav.classList.toggle('open', !expanded);
  });

  document.querySelectorAll('.site-nav a').forEach(link => {
    link.addEventListener('click', () => {
      els.navToggle?.setAttribute('aria-expanded', 'false');
      els.siteNav.classList.remove('open');
    });
  });
}

function initModal() {
  document.querySelectorAll('[data-close-modal="true"]').forEach(el => el.addEventListener('click', closeModal));
  els.modalClose?.addEventListener('click', closeModal);
  document.addEventListener('keydown', e => {
    if (e.key === 'Escape' && els.modal.getAttribute('aria-hidden') === 'false') closeModal();
  });
}

function initLanguageButtons() {
  els.langButtons.forEach(btn => btn.addEventListener('click', () => setLanguage(btn.dataset.lang)));
}

function initThemeToggle() {
  els.themeToggle?.addEventListener('click', toggleTheme);
}

function initCursorGlow() {
  const prefersReduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
  const finePointer = window.matchMedia('(pointer: fine)').matches;
  if (!els.cursorGlow || prefersReduced || !finePointer) return;

  let rafId = null;
  let x = window.innerWidth / 2;
  let y = window.innerHeight / 3;

  const paint = () => {
    els.cursorGlow.style.left = `${x}px`;
    els.cursorGlow.style.top = `${y}px`;
    rafId = null;
  };

  const update = event => {
    x = event.clientX;
    y = event.clientY;
    document.body.classList.add('pointer-active');
    if (!rafId) rafId = requestAnimationFrame(paint);
  };

  window.addEventListener('pointermove', update, { passive: true });
  window.addEventListener('pointerdown', update, { passive: true });
  document.addEventListener('mouseleave', () => document.body.classList.remove('pointer-active'));
}

function init() {
  applyTranslations(state.lang);
  setTheme(state.theme);
  els.year.textContent = new Date().getFullYear();
  initLanguageButtons();
  initThemeToggle();
  initFilters();
  initModal();
  initReveal();
  initNav();
  initCursorGlow();
  loadPortfolio();
}

init();
